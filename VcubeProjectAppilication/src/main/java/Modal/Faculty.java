package Modal;

public class Faculty {
	String FacultyFistname;
	String FacultyLastname;
	String FacultySubject;
	String PhoneNumber;
	String Mail;
	String Adresh;
	int cid;
	public String getFacultyFistname() {
		return FacultyFistname;
	}
	public void setFacultyFistname(String facultyFistname) {
		FacultyFistname = facultyFistname;
	}
	public String getFacultyLastname() {
		return FacultyLastname;
	}
	public void setFacultyLastname(String facultyLastname) {
		FacultyLastname = facultyLastname;
	}
	public String getFacultySubject() {
		return FacultySubject;
	}
	public void setFacultySubject(String facultySubject) {
		FacultySubject = facultySubject;
	}
	public String getPhoneNumber() {
		return PhoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		PhoneNumber = phoneNumber;
	}
	public String getMail() {
		return Mail;
	}
	public void setMail(String mail) {
		Mail = mail;
	}
	public String getAdresh() {
		return Adresh;
	}
	public void setAdresh(String adresh) {
		Adresh = adresh;
	}
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	
	
	

}
